// Assignment Operators

// Assignment Operator
let x = 5;

// Addition Assignment
let x = x + 1;
x += 1;

// Subtraction Assignment
let x = x - 1;
x -= 1;

//Mulitplication Assignment
x = x * 1;
x *= 1;

//Division Assignment
x = x / 1;
x /= 1;

//Remained/Modulus Assignment
x = x % 1;
x %= 1;

//Exponential Assignment
x = x ** 1;
x **= 1;